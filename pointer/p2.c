/*Write a c program to print the number that is repeated and print times of repeated*/

#include <stdio.h>
#define MAX_SIZE 100
void main()
{
    int arr[MAX_SIZE],n,i;
    printf("Enter the number element of array\n");
    scanf("%i",&n);
    printf("Enter the element of array\n");
    for(i=0;i<n;i++)
    {
        scanf("%i",&arr[i]);
    }
}




